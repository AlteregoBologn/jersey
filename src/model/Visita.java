package model;

public class Visita {

	private String diagnosi;
	private String note;
	private String unid;

	public void setDiagnosi(String a) {

		this.diagnosi = a;
	}
	public String getDiagnosi() {

		return diagnosi;
	}
	public void setNote(String a) {

		this.note = a;
	}
	public String getNote() {

		return note;
	}
	public void setUnid(String a) {

		this.unid = a;
	}
	public String getUnid() {

		return unid;
	}
}
